def рассчитать_стоимость_доставки(этаж, тип_лифта, размер, расстояние, срочность):
    стоимость_за_этаж = 100  # Стоимость доставки за этаж без лифта
    скидка_грузовой_лифт = 0.3  # Скидка при наличии грузового лифта
    скидка_обычный_лифт = 0.1  # Скидка при наличии обычного лифта
    коэффициент_размера = {'маленькая': 1.0, 'средняя': 1.5, 'большая': 2.0}
    коэффициент_срочности = {'обычная': 1.0, 'срочная': 1.5}

    # Рассчитываем стоимость за этаж с учетом типа лифта
    if тип_лифта == 'грузовой':
        стоимость_за_этаж *= (1 - скидка_грузовой_лифт)
    elif тип_лифта == 'обычный':
        стоимость_за_этаж *= (1 - скидка_обычный_лифт)

    # Рассчитываем общую стоимость
    общая_стоимость = (стоимость_за_этаж * этаж +
                       расстояние * 50) * коэффициент_размера[размер]
    общая_стоимость *= коэффициент_срочности[срочность]

    return общая_стоимость

# Пример использования функции
этаж = int(input("Введите номер этажа: "))
тип_лифта = input("Введите тип лифта (грузовой/обычный/нет): ").lower()
размер = input("Введите размер мебели (маленькая/средняя/большая): ").lower()
расстояние = float(input("Введите расстояние до места доставки в км: "))
срочность = input("Введите срочность доставки (обычная/срочная): ").lower()

стоимость_доставки = рассчитать_стоимость_доставки(этаж, тип_лифта, размер, расстояние, срочность)
print(f"Стоимость доставки мебели: {стоимость_доставки} руб.")